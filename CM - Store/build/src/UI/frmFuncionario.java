    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Funcionario fu = new Funcionario();
        fu.setCpf(cpf_func.getText());
        fu.setRg(rg_func.getText());
        fu.setPis(pis_func.getText());
        fu.setNct(nct_func.getText());
        fu.setNome(nome_func.getText());
        fu.setEnd(end_func.getText());
        fu.setTel(tel_func.getText());
        fu.setCel(cel_func.getText());
        fu.setSexo(sex_func.getSelectedItem().toString());
        fu.setPass(Arrays.toString(pass_func.getPassword()));
        fu.setCat(cat_func.getSelectedItem().toString());
        if ((pis_func.getText().isEmpty()) || (pass_func.getText().isEmpty()) || (cel_func.getText().isEmpty()) || (cpf_func.getText().isEmpty()) || (rg_func.getText().isEmpty()) || (nct_func.getText().isEmpty()) || (nome_func.getText().isEmpty()) || (end_func.getText().isEmpty()) || (tel_func.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Os campos não podem estar vazios. Favor preencher todos os campos", "Creative Mind Corporation - CM Store v1 | Aviso - Cadastro de Funcionários", JOptionPane.INFORMATION_MESSAGE, aviso);
        } else {

            try {

                FuncionarioController fc = new FuncionarioController();
                fc.cadastra(fu);
                cod_func.setText("");
                cpf_func.setText("");
                rg_func.setText("");
                pis_func.setText("");
                nct_func.setText("");
                nome_func.setText("");
                sex_func.setSelectedItem("Selecione");
                end_func.setText("");
                cat_func.setSelectedItem("Selecione");
                tel_func.setText("");
                cel_func.setText("");
                pass_func.setText("");
                pesq_func.setText("");
                lista_func();
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(frmFuncionario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

